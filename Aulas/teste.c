/* Dada uma sequência de n valores 
inseridos pelo usuário, com n>2, 
faça um programa que
devolva a média dos valores ímpares 
e a soma dos valores pares. */

#include<stdio.h>
int main(){
    int pares, pares2, impares, impares2;

    pares = 26/2;
    pares2 = 26%2;

    impares = 79/2;
    impares2 = 79%2;

    printf("\npares %d\n", pares);
    printf("\npares2 %d\n", pares2);
    printf("\nimpares %d\n", impares);
    printf("\nimpares2 %d\n", impares2);


    return 0;
}
